def plot_all_pie_charts(
	df: pd.DataFrame,
	*,
	save_path: Optional[Path] = None,
	show: bool = True,
	save_svg: bool = False,
	svg_filename: Optional[str] = None,
) -> plt.Figure:
	"""
	Create a 2x2 subplot with all four pie charts: Nest Made, Lethargy, CA Spot Digging, and Anxious Behaviors.
	Uses a single common legend since all charts share the same Yes/No color scheme and sample size.
	
	Parameters:
		df: Master DataFrame
		save_path: If provided, saves the figure to this path
		show: If True, calls plt.show() at the end
		save_svg: If True, saves as SVG in current working directory
		svg_filename: Custom filename for SVG output
		
	Returns:
		The matplotlib Figure object
	"""
	# Clean the dataframe to ensure yes/no conversion to boolean
	cdf = clean_master_dataframe(df)
	
	# Create 2x2 subplot with extra space for legend
	fig, axes = plt.subplots(2, 2, figsize=(14, 12))
	fig.suptitle("Behavioral Observations Distribution", fontsize=16, weight='bold', y=0.995)
	
	# Define the columns and their positions
	columns = [
		("Nest Made?", axes[0, 0]),
		("Lethargy?", axes[0, 1]),
		("CA Spot Digging?", axes[1, 0]),
		("Anxious Behaviors?", axes[1, 1])
	]
	
	# n animals for legend
	n_animals = cdf['ID'].nunique()
	
	for col_name, ax in columns:
		if col_name not in cdf.columns:
			ax.text(0.5, 0.5, f"'{col_name}' not found", ha='center', va='center')
			ax.axis('off')
			continue
		
		# Per-animal mean % Yes and % No (each animal weighted equally)
		animal_yes_pcts = []
		for _, adf in cdf.groupby('ID'):
			valid = adf[col_name].dropna()
			if len(valid) > 0:
				animal_yes_pcts.append(100.0 * valid.sum() / len(valid))
		
		if not animal_yes_pcts:
			ax.text(0.5, 0.5, f"No valid data in '{col_name}'", ha='center', va='center')
			ax.axis('off')
			continue
		
		mean_yes = float(np.mean(animal_yes_pcts))
		mean_no  = 100.0 - mean_yes
		labels = ["Yes", "No"]
		values = [mean_yes, mean_no]
		
		# Use blue for Yes and orange for No
		colors = []
		for label in labels:
			if label == "Yes":
				colors.append("blue")
			else:
				colors.append("orange")
		
		# Create pie chart without labels (we'll use a common legend)
		wedges, texts, autotexts = ax.pie(
			values,
			autopct='%1.1f%%',
			startangle=90,
			colors=colors,
			textprops={'fontsize': 11, 'weight': 'bold'},
		)
		
		# Make percentage text white for better contrast
		for autotext in autotexts:
			autotext.set_color('white')
			autotext.set_fontsize(13)
		
		# Equal aspect ratio ensures that pie is drawn as a circle
		ax.axis('equal')
		
		# Set title for each subplot (removed n from individual titles)
		ax.set_title(f"{col_name}", fontsize=13, weight='bold', pad=10)
	
	# Create a single common legend at the bottom center
	legend_labels = ['Yes', 'No']
	legend_colors = ['blue', 'orange']
	legend_handles = [plt.Rectangle((0, 0), 1, 1, fc=color) for color in legend_colors]
	
	# Add legend below the plots
	fig.legend(
		legend_handles,
		legend_labels,
		loc='lower center',
		ncol=2,
		fontsize=12,
		frameon=True,
		title=f'n = {n_animals} animals',
		title_fontsize=12
	)
	
	# Adjust layout to make room for legend
	plt.tight_layout(rect=[0, 0.03, 1, 0.96])
	
	if save_path is not None:
		fig.savefig(str(save_path), dpi=200, bbox_inches="tight")
	
	if save_svg:
		base = svg_filename or "all_behavioral_pie_charts"
		safe = re.sub(r"[^A-Za-z0-9._-]+", "-", str(base)).strip("-_.") or "plot"
		if not safe.lower().endswith(".svg"):
			safe += ".svg"
		out_path = Path.cwd() / safe
		fig.savefig(str(out_path), format="svg", bbox_inches="tight")
		print(f"Saved SVG to: {out_path}")
	
	if show:
		plt.show()
	
	return fig


