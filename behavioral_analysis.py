def perform_mcnemar_posthoc_behavioral(behavioral_results: dict, df: pd.DataFrame) -> dict:
	"""
	Perform pairwise McNemar tests with Bonferroni correction for behavioral measures.
	
	For measures with significant within-factor effects (Cochran's Q), performs all pairwise
	McNemar tests to identify which specific levels differ from each other.
	
	McNemar's test is appropriate for paired categorical data (same subjects measured
	at different within-factor levels).
	
	Parameters:
		behavioral_results: Dictionary from perform_two_way_chi_square_behavioral
		df: Master DataFrame with columns ID, within-factor column, and behavioral measures
		
	Returns:
		Dictionary containing McNemar test results for each measure with significant effect
	"""
	wc  = _MLB['within_col']
	wk  = _MLB['result_within_key']
	k1  = _MLB['mcnemar_key1']
	k2  = _MLB['mcnemar_key2']
	pk1 = _MLB['mcnemar_prop_key1']
	pk2 = _MLB['mcnemar_prop_key2']
	cl1 = _MLB['mcnemar_col_label1']
	cl2 = _MLB['mcnemar_col_label2']

	if not HAS_STATSMODELS:
		print("\nWARNING: statsmodels not installed. Cannot perform McNemar tests.")
		return {}
	
	# Clean and prepare data
	cdf = clean_master_dataframe(df)
	cdf = _add_day_number_column(cdf)
	if EXPERIMENT_MODE == 'nonramp':
		cdf = _add_week_column(cdf)
	
	# Ensure within-factor column exists
	if wc not in cdf.columns:
		print(f"\nWARNING: '{wc}' column not found. Cannot perform McNemar tests.")
		return {}
	
	measure_mapping = {
		'Nest Made': 'Nest Made?',
		'Lethargy': 'Lethargy?',
		'CA Spot Digging': 'CA Spot Digging?',
		'Anxious Behaviors': 'Anxious Behaviors?'
	}
	
	mcnemar_results = {}
	
	for measure_name, column in measure_mapping.items():
		if measure_name not in behavioral_results:
			continue
		
		results = behavioral_results[measure_name]
		
		# Check if within-factor effect is significant
		wf_significant = results.get(wk, {}).get('significant', False)
		
		if not wf_significant:
			continue
		
		if column not in cdf.columns:
			continue
		
		# Prepare data for this measure
		measure_df = cdf[["ID", wc, column]].copy()
		measure_df = measure_df.dropna(subset=["ID", wc, column])
		
		# Convert to binary (1 = Yes, 0 = No)
		measure_df["Response"] = measure_df[column].apply(
			lambda x: 1 if (x is True or str(x).strip().upper() in ["YES", "TRUE", "1"]) else 0
		)
		
		# Get list of within-factor levels
		levels = sorted(measure_df[wc].unique())
		
		if len(levels) < 2:
			continue
		
		# Perform all pairwise McNemar tests
		pairwise_comparisons = []
		
		from itertools import combinations
		for lv1, lv2 in combinations(levels, 2):
			# Get data for subjects who have BOTH levels
			df_lv1 = measure_df[measure_df[wc] == lv1][["ID", "Response"]].rename(columns={"Response": cl1})
			df_lv2 = measure_df[measure_df[wc] == lv2][["ID", "Response"]].rename(columns={"Response": cl2})
			
			# Merge to get paired data (only subjects with both measurements)
			paired_df = pd.merge(df_lv1, df_lv2, on="ID")
			
			if len(paired_df) < 5:  # Need sufficient paired observations
				continue
			
			# Create 2x2 contingency table for McNemar test
			table = pd.crosstab(paired_df[cl1], paired_df[cl2])
			
			# McNemar test requires a 2x2 table
			if table.shape != (2, 2):
				# Add missing rows/columns with zeros
				for val in [0, 1]:
					if val not in table.index:
						table.loc[val] = 0
					if val not in table.columns:
						table[val] = 0
				table = table.sort_index().sort_index(axis=1)
			
			# Perform McNemar test
			try:
				result = mcnemar(table, exact=False, correction=True)
				
				# Calculate proportions for each level
				prop_lv1 = paired_df[cl1].mean()
				prop_lv2 = paired_df[cl2].mean()

				# Phi coefficient as effect size: sqrt(chi2 / n)
				phi_coef = float(np.sqrt(result.statistic / len(paired_df))) if len(paired_df) > 0 else float('nan')
				
				pairwise_comparisons.append({
					k1: lv1,
					k2: lv2,
					'n_paired': len(paired_df),
					pk1: prop_lv1,
					pk2: prop_lv2,
					'statistic': result.statistic,
					'p_value': result.pvalue,
					'phi': phi_coef,
					'table': table
				})
			except Exception as e:
				print(f"  Warning: McNemar test failed for {_group_label(lv1)} vs {_group_label(lv2)}: {e}")
				continue
		
		if not pairwise_comparisons:
			continue
		
		# Apply Bonferroni correction
		n_comparisons = len(pairwise_comparisons)
		alpha = 0.05
		bonferroni_alpha = alpha / n_comparisons
		
		for comp in pairwise_comparisons:
			comp['p_adjusted'] = min(comp['p_value'] * n_comparisons, 1.0)
			comp['significant'] = comp['p_adjusted'] < alpha
		
		mcnemar_results[measure_name] = {
			'comparisons': pairwise_comparisons,
			'n_comparisons': n_comparisons,
			'bonferroni_alpha': bonferroni_alpha
		}
	
	return mcnemar_results


