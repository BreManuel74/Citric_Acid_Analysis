						# Use female as reference (0)
						glmm_df['Sex_numeric'] = (glmm_df['Sex'] == 'M').astype(int)
						
						print("Fitting GEE model for binary repeated measures...")
						
						# Sort by ID to ensure proper clustering
						glmm_df = glmm_df.sort_values('ID')
						
