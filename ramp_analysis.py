				lines.append(header)
				lines.append("  " + "-"*60)
				
				sorted_comps = sorted(sex_results['comparisons'], key=lambda x: x['p_adj'])
				
				for comp in sorted_comps:
					comparison_name = f"{comp['group1']} vs {comp['group2']}"
					significance = "Yes" if comp['significant'] else "No"
					
