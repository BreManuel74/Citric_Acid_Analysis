def perform_two_way_anova_weight(df: pd.DataFrame) -> dict:
	"""
	Perform MIXED ANOVA (repeated measures) examining the effects of Sex and CA% on weight measures.
	
	This is a proper repeated measures design:
	- Between-subjects factor: Sex (each mouse has one sex)
	- Within-subjects factor: CA% (same mice measured repeatedly across CA concentrations)
	- Subject identifier: ID
	
	Analyzes:
	- Daily Change
	- Total Change
	
	For each measure, examines:
	- Main effect of Sex (between-subjects)
	- Main effect of CA% (within-subjects, repeated measures)
	- Sex × CA% interaction
	
	Parameters:
		df: Master DataFrame with columns ID, Sex, CA (%), Daily Change, Total Change
		
	Returns:
		Dictionary containing mixed ANOVA results for each measure
	"""
	print("\n" + "="*80)
	print("MIXED ANOVA: SEX (BETWEEN) × CA% (WITHIN-SUBJECTS) EFFECTS")
	print("="*80)
	
	if not HAS_PINGOUIN:
		print("\nERROR: pingouin library is required for mixed ANOVA.")
		print("Please install it with: pip install pingouin")
		print("\nFalling back to standard two-way ANOVA (does NOT account for repeated measures)")
		return _perform_standard_anova_fallback(df)
	
	# Clean and prepare data
	cdf = clean_master_dataframe(df)
	cdf = _add_day_number_column(cdf)
	
	# Filter to exclude Day < 0 (baseline measurements)
	cdf = cdf[cdf["Day"] >= 0]
	
	# Keep only rows with complete data
	required_cols = ["ID", "Sex", "CA (%)", "Daily Change", "Total Change"]
	cdf = cdf.dropna(subset=required_cols)
	
	# Convert CA% to integer for cleaner grouping
	cdf["CA (%)"] = cdf["CA (%)"].astype(int)
	
	print(f"\nData summary after filtering:")
	print(f"  Total observations: {len(cdf)}")
	print(f"  Unique animals (subjects): {len(cdf['ID'].unique())}")
	print(f"  Sexes: {sorted(cdf['Sex'].unique())}")
	print(f"  CA% levels: {sorted(cdf['CA (%)'].unique())}")
	print(f"\nDesign: Mixed (Repeated Measures) ANOVA")
	print(f"  Between-subjects factor: Sex")
	print(f"  Within-subjects factor: CA% (repeated measures)")
	print(f"  Subject identifier: ID")
	
	results = {}
	
	# Analyze each weight measure
	measures = {
		'Daily Change': 'Daily Change',
		'Total Change': 'Total Change'
	}
	
	for measure_name, column in measures.items():
		print(f"\n{'='*60}")
		print(f"Mixed ANOVA for: {measure_name}")
		print(f"{'='*60}")
		
		# Create a complete dataframe for this measure with subject ID
		measure_df = cdf[["ID", "Sex", "CA (%)", column]].copy()
		measure_df = measure_df.rename(columns={column: "Value"})
		
		# Convert CA% to integer for pingouin
		measure_df["CA (%)"] = measure_df["CA (%)"].astype(int)
		
		# Group summary statistics
		print(f"\nGroup sizes:")
		group_counts = measure_df.groupby(["Sex", "CA (%)"]).size().reset_index(name="n")
		for _, row in group_counts.iterrows():
			print(f"  Sex={row['Sex']}, CA%={row['CA (%)']}: n={row['n']}")
		
		# Descriptive statistics by Sex and CA%
		print(f"\nDescriptive statistics:")
		group_stats = measure_df.groupby(["Sex", "CA (%)"])["Value"].agg(['mean', 'std', 'count']).reset_index()
		for _, row in group_stats.iterrows():
			print(f"  Sex={row['Sex']}, CA%={row['CA (%)']}: "
				  f"M={row['mean']:.3f}, SD={row['std']:.3f}, n={row['count']}")
		
		# Check for subjects with data in all CA% levels
		subjects_per_ca = measure_df.groupby('ID')['CA (%)'].nunique()
		total_ca_levels = measure_df['CA (%)'].nunique()
		complete_subjects = subjects_per_ca[subjects_per_ca == total_ca_levels].index.tolist()
		incomplete_subjects = subjects_per_ca[subjects_per_ca < total_ca_levels].index.tolist()
		
		if incomplete_subjects:
			print(f"\nWarning: {len(incomplete_subjects)} subjects missing data in some CA% levels:")
			for subj in incomplete_subjects:
				ca_present = sorted(measure_df[measure_df['ID'] == subj]['CA (%)'].unique())
				print(f"  {subj}: present in CA% {ca_present}")
			print(f"Complete subjects (all CA% levels): {len(complete_subjects)}")
			print(f"Note: Filtering to only subjects with complete data across all CA% levels...")
			
			# Filter to only complete subjects (required for proper repeated measures ANOVA)
			measure_df = measure_df[measure_df['ID'].isin(complete_subjects)].copy()
			print(f"After filtering: {len(measure_df['ID'].unique())} subjects, {len(measure_df)} total observations")
		
		# Perform mixed ANOVA using pingouin
		# dv = dependent variable (Value)
		# within = within-subjects factor (CA%)
		# between = between-subjects factor (Sex)
		# subject = subject identifier (ID)
		try:
			print(f"\nRunning mixed ANOVA...")
			aov = pg.mixed_anova(
				data=measure_df,
				dv='Value',
				within='CA (%)',
				between='Sex',
				subject='ID',
				correction='auto'  # Automatically apply Greenhouse-Geisser correction if needed
			)
			
			print(f"\nMixed ANOVA Results:")
			print(aov.to_string())
			print(f"\nAvailable columns in ANOVA table: {list(aov.columns)}")
			
			# Extract results for each effect
			sex_row = aov[aov['Source'] == 'Sex'].iloc[0]
			ca_row = aov[aov['Source'] == 'CA (%)'].iloc[0]
			# Interaction can be labeled as 'Interaction' or 'Sex * CA (%)' depending on pingouin version
			interaction_mask = (aov['Source'] == 'Interaction') | (aov['Source'] == 'Sex * CA (%)')
			interaction_row = aov[interaction_mask].iloc[0]
			
			# Check if sphericity was violated (for CA% effect)
			sphericity_note = ""
			if 'np2' in aov.columns:  # Partial eta-squared
				eta2_sex = sex_row.get('np2', np.nan)
				eta2_ca = ca_row.get('np2', np.nan)
				eta2_interaction = interaction_row.get('np2', np.nan)
			else:
				eta2_sex = np.nan
				eta2_ca = np.nan
				eta2_interaction = np.nan
			
			print(f"\nFormatted Results:")
			print(f"  Sex (between-subjects): F({sex_row['DF1']:.0f},{sex_row['DF2']:.0f}) = {sex_row['F']:.3f}, p = {sex_row['p-unc']:.4f} {'***' if sex_row['p-unc'] < 0.001 else '**' if sex_row['p-unc'] < 0.01 else '*' if sex_row['p-unc'] < 0.05 else 'ns'}")
			print(f"  CA% (within-subjects): F({ca_row['DF1']:.0f},{ca_row['DF2']:.0f}) = {ca_row['F']:.3f}, p = {ca_row['p-unc']:.4f} {'***' if ca_row['p-unc'] < 0.001 else '**' if ca_row['p-unc'] < 0.01 else '*' if ca_row['p-unc'] < 0.05 else 'ns'}")
			print(f"  Sex × CA%: F({interaction_row['DF1']:.0f},{interaction_row['DF2']:.0f}) = {interaction_row['F']:.3f}, p = {interaction_row['p-unc']:.4f} {'***' if interaction_row['p-unc'] < 0.001 else '**' if interaction_row['p-unc'] < 0.01 else '*' if interaction_row['p-unc'] < 0.05 else 'ns'}")
			
			# Extract and display sphericity information
			sphericity_note = ""
			ca_sphericity_info = {}
			interaction_sphericity_info = {}
			
			print(f"\n  Sphericity Tests (Mauchly's):")
			
			# CA% sphericity - check both possible column name variants
			w_col = 'W-spher' if 'W-spher' in ca_row else 'W-sph'
			p_col = 'p-spher' if 'p-spher' in ca_row else 'p-sph'
			
			if w_col in ca_row and not pd.isna(ca_row[w_col]):
				w_sph_ca = ca_row[w_col]
				p_sph_ca = ca_row.get(p_col, np.nan)
				eps_ca = ca_row.get('eps', np.nan)
				p_gg_ca = ca_row.get('p-GG-corr', np.nan)
				
				ca_sphericity_info = {
					'W': w_sph_ca,
					'p': p_sph_ca,
					'epsilon': eps_ca,
					'p_GG_corrected': p_gg_ca,
					'violated': p_sph_ca < 0.05 if not pd.isna(p_sph_ca) else False
				}
				
				print(f"    CA% effect: W = {w_sph_ca:.4f}, p = {p_sph_ca:.4f}")
				if not pd.isna(eps_ca):
					print(f"    Greenhouse-Geisser ε = {eps_ca:.4f}")
				if p_sph_ca < 0.05:
					print(f"    WARNING: Sphericity assumption VIOLATED (p < .05)")
					print(f"    Using Greenhouse-Geisser corrected p = {p_gg_ca:.4f}")
					sphericity_note = " (GG-corrected due to sphericity violation)"
				else:
					print(f"    Sphericity assumption met (p >= .05)")
			else:
				print(f"    CA% effect: No sphericity test (only 2 levels or not applicable)")
			
			# Interaction sphericity - check both possible column name variants
			w_col_int = 'W-spher' if 'W-spher' in interaction_row else 'W-sph'
			p_col_int = 'p-spher' if 'p-spher' in interaction_row else 'p-sph'
			
			if w_col_int in interaction_row and not pd.isna(interaction_row[w_col_int]):
				w_sph_int = interaction_row[w_col_int]
				p_sph_int = interaction_row.get(p_col_int, np.nan)
				eps_int = interaction_row.get('eps', np.nan)
				p_gg_int = interaction_row.get('p-GG-corr', np.nan)
				
				interaction_sphericity_info = {
					'W': w_sph_int,
					'p': p_sph_int,
					'epsilon': eps_int,
					'p_GG_corrected': p_gg_int,
					'violated': p_sph_int < 0.05 if not pd.isna(p_sph_int) else False
				}
				
				print(f"    Sex × CA% interaction: W = {w_sph_int:.4f}, p = {p_sph_int:.4f}")
				if not pd.isna(eps_int):
					print(f"    Greenhouse-Geisser ε = {eps_int:.4f}")
				if p_sph_int < 0.05:
					print(f"    WARNING: Sphericity assumption VIOLATED (p < .05)")
					print(f"    Using Greenhouse-Geisser corrected p = {p_gg_int:.4f}")
				else:
					print(f"    Sphericity assumption met (p >= .05)")
			else:
				print(f"    Sex × CA% interaction: No sphericity test (only 2 levels or not applicable)")
			
			results[measure_name] = {
				'measure': measure_name,
				'anova_table': aov,
				'sex': {
					'F': sex_row['F'],
					'df_effect': sex_row['DF1'],
					'df_error': sex_row['DF2'],
					'p': sex_row['p-unc'],
					'significant': sex_row['p-unc'] < 0.05,
					'effect_size_eta_squared': eta2_sex if not pd.isna(eta2_sex) else 0.0,
					'type': 'between-subjects'
				},
				'ca_percent': {
					'F': ca_row['F'],
					'df_effect': ca_row['DF1'],
					'df_error': ca_row['DF2'],
					'p': ca_row['p-unc'],
					'p_corrected': ca_row.get('p-GG-corr', ca_row['p-unc']),
					'significant': ca_row['p-unc'] < 0.05,
					'effect_size_eta_squared': eta2_ca if not pd.isna(eta2_ca) else 0.0,
					'type': 'within-subjects (repeated measures)',
					'sphericity_correction': sphericity_note,
					'sphericity': ca_sphericity_info
				},
				'interaction': {
					'F': interaction_row['F'],
					'df_effect': interaction_row['DF1'],
					'df_error': interaction_row['DF2'],
					'p': interaction_row['p-unc'],
					'p_corrected': interaction_row.get('p-GG-corr', interaction_row['p-unc']),
					'significant': interaction_row['p-unc'] < 0.05,
					'effect_size_eta_squared': eta2_interaction if not pd.isna(eta2_interaction) else 0.0,
					'type': 'interaction',
					'sphericity': interaction_sphericity_info
				},
				'group_stats': group_stats,
				'total_n': len(measure_df),
				'n_subjects': len(measure_df['ID'].unique()),
				'complete_subjects': len(complete_subjects),
				'incomplete_subjects': len(incomplete_subjects)
			}
			
		except Exception as e:
			print(f"\nError running mixed ANOVA: {e}")
			import traceback
			traceback.print_exc()
			print("\nFalling back to standard ANOVA (does NOT account for repeated measures)")
			return _perform_standard_anova_fallback(df)
	
	print("\n" + "="*80)
	return results


def perform_tukey_hsd_weight(weight_results: dict, df: pd.DataFrame) -> dict:
	"""
	Perform Tukey HSD post-hoc tests for significant main effects in the mixed ANOVA.
	
	For CA% main effect: Pairwise comparisons across all CA% levels (collapsed across Sex)
	For Sex main effect: Not applicable (only 2 levels - F-test is sufficient)
	For Interaction: Simple effects of CA% within each Sex level
	
	Parameters:
		weight_results: Dictionary from perform_two_way_anova_weight
		df: Master DataFrame with columns ID, Sex, CA (%), Daily Change, Total Change
		
	Returns:
		Dictionary containing Tukey HSD results for each measure
	"""
	tukey_results = {}
	
	# Clean and prepare data
	cdf = clean_master_dataframe(df)
	cdf = _add_day_number_column(cdf)
	
	# Filter to exclude Day < 0 (baseline measurements)
	cdf = cdf[cdf["Day"] >= 0]
	
	# Keep only rows with complete data
	required_cols = ["ID", "Sex", "CA (%)", "Daily Change", "Total Change"]
	cdf = cdf.dropna(subset=required_cols)
	
	# Convert CA% to integer for cleaner grouping
	cdf["CA (%)"] = cdf["CA (%)"].astype(int)
	
	measures = {
		'Daily Change': 'Daily Change',
		'Total Change': 'Total Change'
	}
	
	for measure_name, column in measures.items():
		if measure_name not in weight_results:
			continue
		
		results = weight_results[measure_name]
		
		# Check if CA% main effect is significant
		ca_significant = results.get('ca_percent', {}).get('significant', False)
		interaction_significant = results.get('interaction', {}).get('significant', False)
		
		if not ca_significant and not interaction_significant:
			# Skip if no significant effects involving CA%
			continue
		
		tukey_results[measure_name] = {
			'measure_name': measure_name,
			'ca_pairwise': None,
			'simple_effects': None
		}
		
		try:
			# CA% main effect pairwise comparisons (collapsed across Sex)
			if ca_significant:
				print(f"\nPerforming Tukey HSD for CA% main effect ({measure_name})...")
				
				# Prepare data: all observations with CA% grouping
				ca_data = cdf[['CA (%)', column]].copy()
				ca_data = ca_data.dropna()
				
				# Perform Tukey HSD
				tukey_ca = pairwise_tukeyhsd(
					endog=ca_data[column],
					groups=ca_data['CA (%)'],
					alpha=0.05
				)
				
				# Parse results
				comparisons = []
				summary_data = tukey_ca.summary().data[1:]  # Skip header row
				
				for row in summary_data:
					group1, group2, meandiff, p_adj, lower_ci, upper_ci, reject = row
					
					comparisons.append({
						'group1': f"{int(group1)}%",
						'group2': f"{int(group2)}%",
						'meandiff': float(meandiff),
						'p_adj': float(p_adj),
						'lower_ci': float(lower_ci),
						'upper_ci': float(upper_ci),
						'significant': bool(reject)
					})
				
				tukey_results[measure_name]['ca_pairwise'] = {
					'comparisons': comparisons,
					'tukey_object': tukey_ca
				}
				
			if interaction_significant:
				print(f"\nPerforming simple effects analysis for Sex × CA% interaction ({measure_name})...")
				
				# Simple effects: CA% pairwise comparisons within each Sex
				simple_effects = {}
				
				for sex in sorted(cdf['Sex'].unique()):
					sex_data = cdf[cdf['Sex'] == sex][['CA (%)', column]].copy()
					sex_data = sex_data.dropna()
					
					if len(sex_data) < 2:
						continue
					
					# Perform Tukey HSD for this sex
					tukey_sex = pairwise_tukeyhsd(
						endog=sex_data[column],
						groups=sex_data['CA (%)'],
						alpha=0.05
					)
					
					# Parse results
					comparisons = []
					summary_data = tukey_sex.summary().data[1:]  # Skip header row
					
					for row in summary_data:
						group1, group2, meandiff, p_adj, lower_ci, upper_ci, reject = row
						
						comparisons.append({
							'group1': f"{int(group1)}%",
							'group2': f"{int(group2)}%",
							'meandiff': float(meandiff),
							'p_adj': float(p_adj),
							'lower_ci': float(lower_ci),
							'upper_ci': float(upper_ci),
							'significant': bool(reject)
						})
					
					simple_effects[sex] = {
						'comparisons': comparisons,
						'tukey_object': tukey_sex
					}
				
				tukey_results[measure_name]['simple_effects'] = simple_effects
				
		except Exception as e:
			tukey_results[measure_name] = {
				'measure_name': measure_name,
				'error': f"Error performing Tukey HSD: {str(e)}"
			}
	
	return tukey_results


def display_tukey_hsd_results(tukey_results: dict) -> str:
	"""
	Display Tukey HSD post-hoc test results in a formatted table.
	
	Parameters:
		tukey_results: Dictionary from perform_tukey_hsd_weight
		
	Returns:
		Formatted string with Tukey HSD results
	"""
	if not tukey_results:
		return "\n" + "="*80 + "\nNo significant CA% or interaction effects found - Tukey HSD not needed.\n" + "="*80 + "\n"
	
	lines = []
	lines.append("\n" + "="*80)
	lines.append("TUKEY HSD POST-HOC TEST RESULTS")
	lines.append("="*80)
	lines.append("")
	lines.append("Pairwise comparisons for significant main effects and interactions.")
	lines.append("Alpha = 0.05 (family-wise error rate controlled)")
	lines.append("")
	
	for measure, results in tukey_results.items():
		if 'error' in results:
			lines.append(f"{results['measure_name']}: {results['error']}")
			lines.append("")
			continue
		
		lines.append(f"MEASURE: {results['measure_name'].upper()}")
		lines.append("-"*80)
		lines.append("")
		
		# CA% main effect pairwise comparisons
		if results.get('ca_pairwise'):
			lines.append("CA% Main Effect - Pairwise Comparisons (collapsed across Sex):")
			lines.append("-"*60)
			
			header = f"{'Comparison':<15} {'Mean Diff':<12} {'Adj p-value':<12} {'95% CI Lower':<12} {'95% CI Upper':<12} {'Significant':<12}"
			lines.append(header)
			lines.append("-"*80)
			
			# Sort comparisons by p-value
			sorted_comps = sorted(results['ca_pairwise']['comparisons'], key=lambda x: x['p_adj'])
			
			for comp in sorted_comps:
				comparison_name = f"{comp['group1']} vs {comp['group2']}"
				significance = "Yes" if comp['significant'] else "No"
				
				row = (f"{comparison_name:<15} "
					   f"{comp['meandiff']:<12.3f} "
					   f"{comp['p_adj']:<12.6f} "
					   f"{comp['lower_ci']:<12.3f} "
					   f"{comp['upper_ci']:<12.3f} "
					   f"{significance:<12}")
				lines.append(row)
			
			lines.append("")
			
			# Summary of significant comparisons
			sig_comps = [c for c in results['ca_pairwise']['comparisons'] if c['significant']]
			if sig_comps:
				lines.append(f"Found {len(sig_comps)} significant pairwise difference(s):")
				for comp in sig_comps:
					lines.append(f"  {comp['group1']} vs {comp['group2']}: p = {comp['p_adj']:.4f}")
			else:
				lines.append("No significant pairwise differences found.")
			
			lines.append("")
		
		# Simple effects for interaction
		if results.get('simple_effects'):
			lines.append("Sex × CA% Interaction - Simple Effects (CA% comparisons within each Sex):")
			lines.append("-"*60)
			lines.append("")
			
			for sex, sex_results in sorted(results['simple_effects'].items()):
				lines.append(f"  Sex = {sex}:")
				
