def build_week_series_by_day(df: pd.DataFrame) -> pd.Series:
	"""
	Build a Series indexed by per-mouse Day (integer) giving the Week value.
	Since Week is calculated from Day, all IDs have the same week on each day.
	Excludes Day < 0 (first measurement per animal).
	Requires dates to be parsed.
	"""
	cdf = clean_master_dataframe(df)
	cdf = _add_day_number_column(cdf)
	if "Day" not in cdf.columns:
		raise ValueError("Failed to compute 'Day' column for Week series")
	cdf = _add_week_column(cdf)
	if "Week" not in cdf.columns:
		raise ValueError("Failed to compute 'Week' column")

	# Keep rows with Day >= 0 (exclude first measurement) and Week
	tmp = cdf.dropna(subset=["Day", "Week"]).copy()
	tmp = tmp[tmp["Day"] >= 0]
	if tmp.empty:
		return pd.Series(dtype=float)

	def _mode_or_mean(s: pd.Series) -> float:
		m = s.mode(dropna=True)
		if not m.empty:
			return float(m.iloc[0])
		return float(s.mean())

	week_by_day = (
		tmp.groupby("Day")["Week"].apply(_mode_or_mean).sort_index()
	)
	return week_by_day


