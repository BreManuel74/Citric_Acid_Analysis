def bin_weight_changes_by_week(df: pd.DataFrame) -> dict:
	"""
	Bin Daily Change and Total Change by Week for each animal ID.
	
	For each ID, groups all weight change measurements by their corresponding Week.
	This is useful for analyzing how weight changes vary across different weeks.
	
	Parameters:
		df: Master DataFrame with columns ID, Date, Daily Change, Total Change
		
	Returns:
		dict[str, pd.DataFrame]: Dictionary mapping ID -> DataFrame with columns:
			- Week: The week number (1, 2, 3, etc.)
			- Daily Change: List of all daily change values in that week
			- Total Change: List of all total change values in that week
			- Daily Change Mean: Mean of daily changes in that week
			- Daily Change SEM: Standard error of mean for daily changes
			- Total Change Mean: Mean of total changes in that week
			- Total Change SEM: Standard error of mean for total changes
			- Count: Number of observations in that week
			
	Example:
		>>> binned = bin_weight_changes_by_week(df)
		>>> print(binned['W1 L'])
		   Week  Daily Change Mean  Total Change Mean  Count
		0     1              0.150              0.447      7
		1     2             -0.700             -1.147      7
		...
	"""
	print("\n" + "="*80)
	print("BINNING WEIGHT CHANGES BY WEEK")
	print("="*80)
	
	required = {"ID", "Date", "Daily Change", "Total Change"}
	missing = required - set(df.columns)
	if missing:
		raise ValueError(f"DataFrame missing required columns: {sorted(missing)}")
	
	# Clean the dataframe
	cdf = clean_master_dataframe(df)
	print(f"Initial data: {len(cdf)} rows, {len(cdf['ID'].unique())} unique IDs")
	
	# Add Day and Week columns
	cdf = _add_day_number_column(cdf)
	cdf = _add_week_column(cdf)
	
	if "Week" not in cdf.columns:
		raise ValueError("Failed to compute 'Week' column")
	
	print(f"Day range in data: {cdf['Day'].min()} to {cdf['Day'].max()}")
	print(f"Weeks present: {sorted(cdf['Week'].dropna().unique())}")
	
	# Drop rows with missing essential data and exclude Day < 0 (first measurement)
	before_drop = len(cdf)
	cdf = cdf.dropna(subset=["ID", "Week", "Daily Change", "Total Change"])
	cdf = cdf[cdf["Day"] >= 0]
	print(f"After filtering (excluding Day<0 and NaN): {len(cdf)} rows (dropped {before_drop - len(cdf)} rows)")
	
	binned_by_id = {}
	
	print(f"\nBinning data for {len(cdf['ID'].unique())} animals:")
	for animal_id, group in cdf.groupby("ID", dropna=True):
		print(f"  {animal_id}: {len(group)} measurements across {len(group['Week'].unique())} weeks")
		
		# Group by Week and aggregate
		binned = group.groupby("Week").agg({
			"Daily Change": ["mean", "sem", "count", lambda x: list(x)],
			"Total Change": ["mean", "sem", lambda x: list(x)]
		}).reset_index()
		
		# Flatten column names
		binned.columns = [
			"Week",
			"Daily Change Mean",
			"Daily Change SEM",
			"Count",
			"Daily Change Values",
			"Total Change Mean",
			"Total Change SEM",
			"Total Change Values"
		]
		
		# Reorder columns for clarity
		binned = binned[[
			"Week",
			"Count",
			"Daily Change Mean",
			"Daily Change SEM",
			"Daily Change Values",
			"Total Change Mean",
			"Total Change SEM",
			"Total Change Values"
		]]
		
		print(f"    Week summary for {animal_id}:")
		for _, row in binned.iterrows():
			print(f"      Week {int(row['Week'])}: n={row['Count']}, Daily Δ={row['Daily Change Mean']:.3f}±{row['Daily Change SEM']:.3f}, Total Δ={row['Total Change Mean']:.3f}±{row['Total Change SEM']:.3f}")
		
		binned_by_id[str(animal_id)] = binned
	
	print(f"\nBinning complete: {len(binned_by_id)} animals processed")
	print("="*80 + "\n")
	return binned_by_id


def export_binned_data_to_csv(
	binned_data: dict,
	output_dir: Optional[Path] = None,
	prefix: str = "binned_week"
) -> list[Path]:
	"""
	Export binned weight change data to CSV files.
	
	Creates one CSV file per animal ID with their Week-binned weight changes.
	
	Parameters:
		binned_data: Dictionary from bin_weight_changes_by_week()
		output_dir: Directory to save CSV files (default: current working directory)
		prefix: Prefix for output filenames
		
	Returns:
		List of Path objects for created CSV files
		
	Example:
		>>> binned = bin_weight_changes_by_week(df)
		>>> files = export_binned_data_to_csv(binned, prefix="animal_week_binned")
		>>> print(files)
		[Path('animal_week_binned_W1_L.csv'), Path('animal_week_binned_W2_O.csv'), ...]
	"""
	if output_dir is None:
		output_dir = Path.cwd()
	else:
		output_dir = Path(output_dir)
		output_dir.mkdir(parents=True, exist_ok=True)
	
	created_files = []
	
	for animal_id, data in binned_data.items():
		# Create a copy and format for CSV export
		export_df = data.copy()
		
		# Convert list columns to string representation for CSV
		if "Daily Change Values" in export_df.columns:
			export_df["Daily Change Values"] = export_df["Daily Change Values"].apply(
				lambda x: ";".join(f"{v:.2f}" for v in x) if isinstance(x, list) else str(x)
			)
		if "Total Change Values" in export_df.columns:
			export_df["Total Change Values"] = export_df["Total Change Values"].apply(
				lambda x: ";".join(f"{v:.2f}" for v in x) if isinstance(x, list) else str(x)
			)
		
		# Create safe filename
		safe_id = re.sub(r"[^A-Za-z0-9_-]+", "_", str(animal_id))
		filename = f"{prefix}_{safe_id}.csv"
		filepath = output_dir / filename
		
